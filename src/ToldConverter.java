public class ToldConverter {

    public void toyName (int toy){



        switch(toy)
        {
            case  0:
                System.out.println("Car");
                break;
            case 1:
                System.out.println("Lego");
                break;
            case 2 :
                System.out.println("Doll");
                break;
            case 3 :
                System.out.println("Puzzle");
                break;
            default :
                System.out.println("Illegal");
        }

    }

}
